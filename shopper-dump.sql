CREATE TABLE public."orderProducts" (
	id serial4 NOT NULL,
	"orderId" int4 NOT NULL,
	"productId" int4 NOT NULL,
	amount int4 NOT NULL,
	"addedAt" timestamp(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
	"updatedAt" timestamp(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
	CONSTRAINT "orderProducts_pkey" PRIMARY KEY (id),
	CONSTRAINT "orderProducts_orderId_fkey" FOREIGN KEY ("orderId") REFERENCES public."order"(id) ON DELETE CASCADE,
	CONSTRAINT "orderProducts_productId_fkey" FOREIGN KEY ("productId") REFERENCES public.product(id) ON DELETE CASCADE
);

